
public class karsilastirma_operatorleri {

	public static void main(String[] args) {
	
		        int deger1 = 5;
		        int deger2 = 10;
		        if(deger1 == deger2)
		            System.out.println("deger1 == deger2");
		        if(deger1 != deger2)
		            System.out.println("deger1 != deger2");
		        if(deger1 > deger2)
		            System.out.println("deger1 > deger2");
		        if(deger1 < deger2)
		            System.out.println("deger1 < deger2");
		        if(deger1 <= deger2)
		            System.out.println("deger1 <= deger2");
	}

}
